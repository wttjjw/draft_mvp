import streamlit as st

# Заголовок страницы
st.title("Добро пожаловать на туристическую страницу! 🌍")

# Приветствие
st.write("""
Привет! Мы рады узнать больше о ваших туристических предпочтениях.
Пожалуйста, ответьте на несколько вопросов ниже.
""")

# Имя пользователя
name = st.text_input("Как вас зовут?")

# Тип отдыха
vacation_type = st.selectbox(
    "Какой тип отдыха вы предпочитаете?",
    ["Пляжный", "Городской туризм", "Горы и природа", "Активный отдых", "Культурные поездки"]
)

# Посещенные города России
cities = st.multiselect(
    "Какие города России вы уже посещали?",
    ["Москва", "Санкт-Петербург", "Казань", "Сочи", "Екатеринбург", "Новосибирск", "Владивосток"]
)

# Оценка интереса к путешествиям
travel_rate = st.slider("Насколько вы любите путешествовать?", 1, 10, 5)

# Кнопка отправки
if st.button("Отправить ответы"):
    st.success("Спасибо за ответы!")

    st.write("### Ваши ответы:")
    st.write(f"Имя: {name}")
    st.write(f"Предпочитаемый тип отдыха: {vacation_type}")
    st.write(f"Посещенные города: {', '.join(cities) if cities else 'Пока не посещали'}")
    st.write(f"Любовь к путешествиям (1–10): {travel_rate}")

    st.write("Надеемся помочь вам спланировать следующее путешествие! ✈️")
